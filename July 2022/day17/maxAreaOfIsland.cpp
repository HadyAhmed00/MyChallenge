bool is_Vailed(int i,int j )
{
    return i<width && j<hight && i>=0 &&j>=0;
}
int maxAreaOfIsland(vector<vector<int>>& grid) {
    width = grid.size();
    hight=grid[0].size();
    int dir []={1,0,-1,0,0,1,0,-1};
    int Max = INT_MIN;

    for (int i = 0; i < grid.size(); ++i) {
        for (int j = 0; j < grid[i].size(); ++j) {
            if(grid[i][j]==1){
                stack<pair<int,int>> col;
                map<pair<int,int>,bool> visited;
                int counter =1;
                visited.insert({make_pair(i, j), true});
                col.push({i,j});
                while (!col.empty()){
                    int xx=col.top().first, yy =col.top().second;
                    col.pop();
                    for (int k = 0; k < 8; k+=2) {
                        int x =xx+dir[k]; int y =yy+dir[k+1];
                        if(is_Vailed(x,y))
                        {
                            if(grid[x][y] && visited.find({x, y})==visited.end())
                            {
                                col.push({x,y});
                                counter++;
                                visited.insert({make_pair(x, y), true});
                            }
                        }

                    }
                }
                Max = max(Max,counter);
            }
        }
    }
    return max(Max,0);

}
